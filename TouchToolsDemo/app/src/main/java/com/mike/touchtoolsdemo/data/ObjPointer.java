public class ObjPointer {
    private float x;
    private float y;
    private float touchMajor;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getTouchMajor() {
        return touchMajor;
    }

    public void setTouchMajor(float touchMajor) {
        this.touchMajor = touchMajor;
    }
}
